/****************************************************************************
 * Included Files
 ****************************************************************************/

#include "app.h"

#include "digital_io.h"

 /****************************************************************************
 * Pre-processor Definitions
 ****************************************************************************/

#define DELAY 50000 

/****************************************************************************
 * Private Functions
 ****************************************************************************/

/* Funcao nao portavel. Futuramente resscrever como API do HAL */

void delay(int delay)
{
  while (delay--)
    {
      asm("nop");
    }
}

/****************************************************************************
 * Public Functions
 ****************************************************************************/

/****************************************************************************
 * Name: setup
 ****************************************************************************/

void setup(void)
{
  pinMode(0, OUTPUT);
}

/****************************************************************************
 * Name: loop
 ****************************************************************************/

void loop(void)
{
  /*Liga LED*/

  digitalWrite(0, LOW);
  delay(DELAY);

  /*Desliga LED*/

  digitalWrite(0, HIGH);
  delay(DELAY);
}